                                <li>
                                    <a href="https://es.wikipedia.org/wiki/Michel_de_Montaigne" title="米歇尔·德·蒙田（Michel de Montaigne）" target="_blank">米歇尔·德·蒙田（Michel de Montaigne）</a>
                                    <blockquote>
                                        <p>  蒙田为人平和，新教徒受旧教徒迫害，起来迫害，他认为何必害来害去，即为新教徒辩护，也为旧教徒辩护。<br>
  我总把他看成怀疑主义家谱里的前辈。“将容忍和自尊保持得最好的人。”这是我对他的评价。<br>
  容忍，最大度的容忍，自尊，最高度的自尊。我自勉，也共勉。但很难做到。我在狱中曾经想起蒙田的一句话，这句话，是他引自一位古代水手的：  
                                        </p>
                                        <p>    <strong>哦，上帝，你要救我我就救我，你要毁灭我就毁灭我，但我时时刻刻把持住我的舵。</strong>  
                                        </p>
                                        <p>  另有一句：  
                                        </p>
                                        <p>    <strong>世上最大的事，是一个人知道什么才是他自己的。</strong>  
                                        </p>
                                        <p>  这句话对艺术家很好。人要临危不乱，临幸福也不乱。<br>
  国王对他说：“我喜欢你的书。”他马上说：“那么，你应该喜欢我的人。”可敬可爱！他憎恶狂热的信仰，恐怖的行为，残酷的刑罚。论及“人道主义”，不要忘了蒙田。</p>
                                    </blockquote>
                                    <ul>
                                        <li>《Les Essais》（随笔集）</li>
