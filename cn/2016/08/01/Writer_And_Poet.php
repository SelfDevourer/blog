                                <li>
                                    <strong><a href="https://en.wikipedia.org/wiki/Francis_Bacon" title="弗兰西斯·培根（Francis Bacon）" target="_blank">弗兰西斯·培根（Francis Bacon）</a></strong>
                                    <blockquote>
                                        <p>  培根（Francis Bacon，1561——1626）是政治家、法学家，又是哲学家、文学家，每方面都占很高的地位。知识渊博，博及整个时代，思想感动整个世界，强调知识与实据。思想有血有肉，观点明达。论文集最佳，小说写过《新大西洋岛》（The New Atlantis），实为他的理想国。他将自己的理想写得很仔细。<br>
  他的格言、短句，非常出色。我常爱接引他的话，与他别扭，侃大山。  
                                        </p>
                                    </blockquote>
                                </li>
                                <li>
                                    <strong><a href="https://en.wikipedia.org/wiki/William_Shakespeare" title="威廉·莎士比亚（William Shakespeare）" target="_blank">威廉·莎士比亚（William Shakespeare）</a></strong>
                                    <blockquote>
                                        <p>   剧中人物的身份和性格非常复杂，凡人、名人、仙人，一上台几句话，个性毕现，忘不了。这是极大特点。艺术家呈现这个世界，唯一的依本，就是他自己。<br>
  莎士比亚表现莎士比亚。<br>
  ……<br>
  你们看书可惜太少。不但少，遍数也太少。莎剧，我看过五六十遍，为什么呢？年年中秋吃月饼，多少月饼？上礼拜堂，天天上。福音书，我读过百多遍。每次读都不一样，到老也读不透的。<br>
  有人一看书就卖弄。多看几遍再卖弄吧——多看几遍就不卖弄了。<br>
  ……<br>
  不三不四的爱，倒是和死不相干。<br>
  ……<br>
  人世真没意思，因为真没意思，艺术才有意思。<br>
  到了老年，莎士比亚似乎把郁结心中的哲学观点都放到丹麦王子形象上，但仿佛都是哈姆雷特、而不是莎翁所言。你们看原作，哈姆雷特和人的对白，与他自己的独白，完全是两种辞令语调，这是剧作者莎士比亚在遥控。是莎士比亚与哈姆雷特血肉相连，但又离得很远，远远地“遥控”。<br>
  许多作家喜欢死乞白赖地赖在角色身上，喜欢靠角色来说自己的话——用这个准则对照大批著名文学家，也不例外。可是莎士比亚、普希金、陀思妥耶夫斯基、福楼拜、司汤达、哈代、巴尔扎克、从不和剧中人发生暧昧关系——哈姆雷特是莎士比亚的精神上的儿子。可是这位父亲一点不通私情，冷静看他儿子表演。<br>
  哈姆雷特是个悲观主义者，但确实享乐主义者，是个思想者，不肯行动，觉得自“我在思想里已做过一回了”。一个思想过度的人，行动非常软弱。<br>
  许多人不死，拖拖拉拉活下去，因为在思想上已经死过了。我要是续写《红楼梦》，会让贾宝玉拖拖拉拉活下去。<br>
  《哈姆雷特》其中有个最坏的叔叔，他却不多写。后来我懂了，有个象征即可，不必多谢。整个古堡阴沉，唯奥菲莉娅（Ophelia）的死是明艳的一笔，白色和绿色。<br>
