  帕斯卡天性非常好，文笔清如水，我永远尊敬他。刚才那句话像谁的口气？像耶稣。“你们到田野里看什么？看芦苇吗？看先知吗？他比先知大得多了。”他还说：“人只有靠思想才能伟大。”<br>
