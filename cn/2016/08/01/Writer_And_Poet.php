  后来的思想被人发展：人是靠思想这个东西和宇宙对抗的。宇宙大，人小，人知道。宇宙无情，人也知道，这都是人厉害的地方，人类最宝贵的一点。<br>
  这是个伟大的数学家，虔诚的神学家，又是个优秀的文学家、思想家。</p>
