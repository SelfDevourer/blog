                                    安东尼·契诃夫(Anton Chekhov)
                                    <blockquote>
                                        <p>  契诃夫作为一个人，非常有意思。谦和、文静、克制、优雅，通达人情。高尔基回忆契诃夫，写得好！<br>
  半夜了，天很冷，契诃夫打电话：“请你来一下。“什么事？”“我恋爱了。”高尔基去，哪里是恋爱，只是与他谈谈，走走，然后说：“你可以回去了。”<br>
                                            他常教高尔基写作。怎样写作呢？契诃夫说：“下雨了。”就这样写。<br>
  契诃夫始终方寸不乱。<br>
  高尔基与契诃夫的通信极好：<br>
 <br>
    <strong>今天收到你寄给我的表，我真想上街拦住那些人，说：“你们这些鬼，知道吗？契诃夫送给我一只表！”</strong><br>
 <br>
  多可爱！<br>
  契诃夫不是一个思想家。他知道俄国是不幸的，常在忧伤中，他对未来，一片茫然。如果问他，他大概说：“总会好起来吧。”那潜台词是：“否则怎么办呢。”<br>
  他和同代人比，有教养。当时俄罗斯有两种类型。要么是狂热的，战斗的，革命的，要么是悲观颓废的，悲观到要杀人，自杀。相较之下，有那么一个契诃夫，特别宝贵。<br>
  他说，短篇，莫泊桑已是王，不过大狗叫，小狗也要叫——这点自知之明，也多么宝贵。<br>
  说到短篇，二十世纪远高于十九世纪。<br>
  契诃夫的短篇，写得太通俗。一定要说他的成就，现在冷静比较，比下去了。鲁迅说契诃夫的小说是“含泪的微笑”，中学水准。我以为，文学不需要含泪，也不需要微笑。<br>
  艺术是不哭的，也不笑的。</p>
