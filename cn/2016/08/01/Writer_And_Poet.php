                            <h3>第三十一讲 文艺复兴与莎士比亚</h3>
                            <ul>
                                <li>
                                    <a href="https://en.wikipedia.org/wiki/Niccol%C3%B2_Machiavelli" title="**尼科洛·迪贝尔纳多·代·马基雅弗利（Niccolò di Bernardo dei Machiavelli）**" target="_blank"><strong>尼科洛·迪贝尔纳多·代·马基雅弗利（Niccolò di Bernardo dei Machiavelli）</strong></a>
                                    <blockquote>
                                        <p>  我可大胆地说，人类中最具有独特性格的就是马基雅维利。他怪在判断力、观察力非常敏锐，但办起事来非常率性，老要人做坏事，自己不做坏事。<br>
  他自称是人类的公共秘书。所谓“风声雨声声声入耳，家事国事事事关心”，即马基雅维利。<br>
  他是个怪杰。你们翻翻世界伟人的辞典，耶稣、苏格拉底、帕拉图……马基雅维利总是位列其中。他的《君主论》，讲韬略，讲权谋，与中国兵法比，更赤裸裸谈论权利与统治。中国讲权谋，有遮羞布、光子、大旗，马基雅维利直截了当讲。<br>
  中国的圣人教人做好事，自己不做。马基雅维利叫人做坏事，自己不做。他就事论事的那一套，与理想主义相反。 <br>
  ……<br>
  马基雅维利的观点或办法，应该看作一种反讽。你和政治恶魔周旋，你要知己知彼。你讲实话，我比你更实际。你同恶人打交道，要恶过他的头。中国古代的所有军师斗法，都是马基雅维利一路。向来以为他公开宣扬恶，以恶制恶，我以为他是反讽。</p>
                                    </blockquote>
