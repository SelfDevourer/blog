                            <h3>第三十七讲 歌德、席勒及十八世纪欧洲文学</h3>
                            <ul>
                                <li>
                                    约翰·沃尔夫冈·冯·歌德(Johann Wolfgang von Goethe)
                                    <blockquote>
                                        <p>  ……寿长，横跨两个世纪。殁于十九世纪中叶。幼时爱听母亲讲神话——最初的家教啊！感慨啊！以前母亲、祖母、外婆、保姆、佣人讲故事给小孩听，是世界性好传统。有的母亲讲得特别好，把自己放进去。<br>
  这种非功利性的教育，渗透孩子的心灵。如这孩子天性高，这就是他日后伟大成就的最初种子。<br>
  现在，这传统世界性地失去了。现在的电视教育，就是教人无耻——教得很成功。<br>
  歌德的父亲，正派人物，要儿子学法律。但他的兴趣在文艺、绘画、雕塑。他去作画，雕刻，恋爱，写剧本，经营剧场（他的素描不在诸位之下），母亲赞成，父亲不喜。<br>
  “天行健，君子以自强不息”就是歌德，也即浮士德（Faust）这部作品的精神。整个西方文化即浮士德精神。中国也有少数智者知道阳刚是正途，自强是正道，但一上来就趋于阴柔。<br>
  我主张正道，是正面地、直接地去阳刚，不得已时，阴柔。<br>
  西方文化是阳刚的，男性的，力夺的；中国文化是阴柔的，女性的，智取的——不过，这是指过去的传统。现在东西方文化都败落了，谈不上了。<br>
  他的相貌，体格，也完美体现浮士德精神。死后，人揭布窥其尸，无一处赘肉，无一处枯瘦。<br>
  歌德少年时画画，青年时代到意大利开眼，窥视了艺术的殿堂，从此放弃画画。说：我不是画画的。会画画的人，不画时，技巧会进步的。我一不画，就退步了。<br>
  他能这么想，了不起。说是退步，其实是自强。一个人能这么冷贤，第一，是能旁观自己，第二，是能知道自己，做自己的良师益友。
  画画不画画，不要紧。这种公正的自我判断，才是造成大师的因素之一。<br>
  这是小事，大有深意。<br>
  读书，要确切理解作者的深意，不要推想作者没有想到的深意。上帝创造这世界，但他不理解这世界；艺术家创造了这世界，他理解这世界。<br>
  《少年维特之烦恼》（Die Leiden des jngen Werthers），大家可以再读读。我最近又读，很好，元气淋漓。<br>
  文学要有读者，宿命的是，文学很难得到够格的读者。当时多少少年读《维特》后都自杀，这种读者我不要。至少不提倡这种作者与读者的关系。<br>
  任何作者，很难看穿读者。老子说“天地不仁，以万物为刍狗”（世界上最光辉的警句：一，想到了；二，说出来了；三，讲得这么美妙），我说：作家不仁，以读者为刍狗。<br>
  这样天地才大，这样才能有伟大的读者来。最好读者也不仁——作者不仁，读者不仁，如此，“仁”来了。<br>
  ……<br>
  艺术不是以量取胜。但解决了量的问题后（求质），则量越多越好。一个人有无才能，是一回事；有才能，能不能找到好题材，又是一回事。许多人才高，一辈子找不到好题材，使不上好方法，郁郁终生。<br>
  圣伯夫（Sainte——Beuve）给了福楼拜题材。福楼拜先写过《圣安东尼的诱惑》，宗教一类，圣伯夫请福楼拜看纪实新闻，遂成《包法利夫人》。<br>
  在座各位，就是苦于找不到题材，找不到方法。怎么找法？只有拼命去找。找不到，自我埋没；找到了，自强，参悟。<br>
  ……<br>
  如果把《浮士德》看成全世界文学顶峰，全世界错。<br>
  浮士德是北欧民间传说中的炼金术师，性格模糊，形象也窝囊，近乎妖道。歌德借了这题材，把浮士德提高到整个欧罗巴文化的精神象征，这是他了不起的功绩，我由衷钦佩。从文学角度说，《浮士德》不成功；从文化现象讲，《浮士德》伟大。<br>
  我承认《浮士德》在命题上的伟大。<br>
  约翰·克里斯多夫，算是一种“典型”的期望。是什么呢？什么也不是。典型是牵强附会的，见树不见林的，一厢情愿的。如果艺术不伟大，不可能表达民族。血是艺术家自己的血，血管是民族文化的血管——才行。<br>
  伟大的艺术来自伟大的性格，艺术是无法培养的。<br>
  “与公瑾交，若饮醇醪，不觉自醉。”性格交友要锻炼到如此。<br>
  歌德所谓自强：他最会自我教育。约八十次恋爱，可是都成功，因为他迷途知返。我说：“恋爱总会成功的。”为什么呢？你爱，那就成功了。歌德曾说：“假如我爱你，与你无涉。”全世界欣赏这句话。<br>
  他有格言：回到内心。其实陶潜的《归去来兮辞》，就是回到内心。要学会自我教育，才能有良师益友。生活上可以做光棍，精神上可别做光棍。<br>
  《歌德对话录》，是我们艺术家的复印。我最早的信来自歌德，心中安安大喜。纪德说得好：歌德不是高山，不是大海，他是阳光充足雨露滋润的半高原。<br>
  海涅一贯调皮，得理不饶人。他说：“歌德老是坐着的，好多事需要他站起来，才能解决，但歌德坐着也是对的。”庙堂里的佛像都是坐着的，如果站起来，岂非庙堂的顶要破。<br>
  ……<br>
  自信，必须要的，这可测试一个人高贵卑下。见名人，要见其人，不见其名。歌德去见拿破仑，拿破仑站起来，向群臣说：“看，这个人。”<br>
  这是当年耶稣出现时，罗马总督彼拉多说的话，尼采拿来作书名（《瞧，这个人》）。<br>
  大多数人是只见其名，不见其人。  
                                        </p>
