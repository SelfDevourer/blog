                                <li>
                                    <a href="https://es.wikipedia.org/wiki/Miguel_de_Cervantes" title="v米格尔·德·塞万提斯·萨维德拉（Miguel de Cervantes Saavedra）" target="_blank">米格尔·德·塞万提斯·萨维德拉（Miguel de Cervantes Saavedra）</a>
                                    <blockquote>
                                        <p>  从前骑士很惨，晚年退休后没有保障，弱者当雇佣兵，强者当强盗。制度没落了，塞万提斯就写骑士。写到后来，他自己的血液流到主角身上，成为骑士的意念，觉得骑士的善良被浪费了。惟其浪费，才是真的善良。这是塞万提斯的伟大的善良。<br>
  ……<br>
  三十四岁写作（大家三十多岁写作，不迟啊），一边任小职。脾气不好，常入狱。出书后成名，一点好处也没有，还要饭。后十年写成《堂吉诃德》（Don Quijote）。这是一本以嘲笑开始、以祈祷结束的伟大的人道主义的杰作。骑士的行径怪诞不经，悖于情理，可是你读着读着，会深深同情他。这就是塞万提斯的文学魅力。少年人读《堂吉诃德》，不会懂的。  
                                        </p>
                                    </blockquote>
                                    <ul>
                                        <li>《Don Quijote de la Mancha》（堂吉诃德）</li>
                                    </ul>
                                </li>
                                <li>
                                    <a href="https://en.wikipedia.org/wiki/Desiderius_Erasmus" title="德西德里乌斯·伊拉斯谟（Erasmus (Desiderius) von Rotterdam）" target="_blank">德西德里乌斯·伊拉斯谟（Erasmus (Desiderius) von Rotterdam）</a>	
                                    <blockquote>
                                        <p>   伊拉斯谟曾得教皇与各国国王请教，他已拉丁文写作，作品宏富，最著者有《愚人颂》（The Praise of Fally），讽刺世俗的众生。一出七版。什么缘故？我以为每个世俗的人都喜欢骂世俗的书，以此认为自己不俗。</p>
                                    </blockquote>
                                    <ul>
                                        <li>《The Praise of Fally》（愚人颂）</li>
                                    </ul>
                                </li>
                                <li><a href="https://es.wikipedia.org/wiki/Tom%C3%A1s_Moro" title="托马斯·莫尔（Thomas More）" target="_blank">托马斯·莫尔（Thomas More）</a></li>
                                <li>罗伯特·赫里克（Robert Herrick）</li>
                                <li>托马斯·怀特（Thomas Wyatt）</li>
