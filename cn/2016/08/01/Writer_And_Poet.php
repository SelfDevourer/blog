                                    <blockquote>
                                        <p>  文艺复兴是苦行主义的中古精神之后的求知、行乐、进取、妩媚，这些精神正好集中于拉伯雷作品。拉伯雷年青时是教士，后来抛弃顺利的生活，成为在家的修士（也有修道者离开家庭）。他有三十多年传道生活，深知幽闭禁欲的坏处苦处。<br>
  我们和拉伯雷相隔数百年，我们也深知幽闭禁欲的坏处，深知幽闭禁欲的苦处。<br>
  拉伯雷老在作品中嘲笑圣女，扩大到嘲笑一切人和事物，刻骨铭心的嘲笑，但不冷酷，不愤怒。他的滑稽是淳朴的、忠厚的，他的深刻性正在于此。我曾给他一个名称，叫做“敏感的人道主义者，粗鲁的文学家”。  
                                        </p>
                                    </blockquote>
                                    <ul>
                                        <li>《Pantagruel》（巨人传）</li>
