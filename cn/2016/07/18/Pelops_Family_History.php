                            <h1>Pelops家族史</h1><hr>
                            <p>在希腊神话中，Atreus（阿特柔斯）是伯罗奔尼撒半岛迈锡尼国王，Pelops（彼洛普斯）和Hippodamia（希波达米娅）的儿子，Agamemnon（阿伽门农）和Menelaus（墨涅拉俄斯）的父亲。他的后裔称为Atreidai或者Atreidae。  
                            </p>
                            <p>Atreus和他的兄弟Thyestes（堤厄斯忒斯）因为谋杀他们同父异母的兄弟Chrysippus而被父亲放逐，他们在迈锡尼避难，迈锡尼国王Eurystheus(欧里斯修斯)攻打Heracleidae（赫拉克勒斯的后裔）期间任命他们代使王权，但在Eurystheus战死后王权永远属于Atreus了。  
                            </p>
                            <p>根据众多古籍记载，Atreus是Pleisthenes的父亲，但是一些抒情诗人（Ibycus（伊比库斯）、Bacchylides（巴库利德斯））将Pleisthenes用于Atreus的代称。  
                            </p>
                            <p>Atreus一族发源于Tantalus。Tantalus是Zeus的儿子，曾与众神有着亲切的关系，直到他杀了自己的儿子Pelops作为众神的牺牲以测试众神是否无所不知。当神祗们与Tantalus一起享用晚餐，立刻明白发生了什么，只有Demeter（得墨忒尔）由于女儿Persephone（普西芬妮）被Hades（哈迪斯）绑架而心烦意乱吃下了Pelops的肩。众神将Tantalus放逐到冥界，他永恒的站在水池中果树树枝下，每当他想要吃果实时树枝就升到他抓不住的高度，每当他弯下腰想喝水时，水位就降到他能喝到的地方以下。从而派生了单词“tantalising”，诱人的意思。众神复活了Pelops，用象牙替代了他的肩胛骨。从而能够衍生一个大家族。</p>
                            <p>Pelops与Oenomaus进行战车比赛并胜利，迎娶Oenomaus的女儿Hippodamia。Pelops破坏了Oenomaus的战车车轮并导致其在战斗中死亡。在不同的故事版本里，Pelops以Hippodamia的初夜权诱惑Oenomaus的仆人Myrtilus，指使他破坏Oenomaus的车轮。然而事成后Pelops不愿分享战果（Hipodamia),Myrtilus死前诅咒Pelops家族，使得他们的罪孽愈发沉重。</p>
                            <p>Pelops和Hippodamia有很多儿子，其中两位是Atreus（阿特柔斯）和Thyestes（堤厄斯忒斯）。在神话中，他们谋杀了同父异母的兄弟Chrysippus（克吕西波斯）。因谋杀罪Hippodamia、Atreus和Thyestes被放逐到迈锡尼，Hippodamia据说已经上吊自尽。</p>
                            <p>Atreus从他的羊群中找出最好的羊羔作为牺牲献给Artemis，Atreus发现金羊羔后将它交给他的妻子Aerope（埃罗配）而不是献给Artemis。Aerope将金羔羊送给了Thyestes，因为她爱着Atreus的兄弟。Atreus后来提出持金羊羔者王，Thyestes上位。 </p>
                            <p>后来Atreus凭借Hermes的神谕重夺王座并将Thyestes放逐。Thyestes发誓如果太阳在天空中后移就将王王国给予Atreus，这个誓言Zeus实现了。</p>
                            <p>Atreus知道Thyestes和Aerope通奸后便计划复仇，他杀掉和烹饪Thyestes的儿子们，保存他们的手和脚，骗Thyestes吃下他孩子们的肉然后嘲笑他的手脚。随后Thyestes也因为吃人肉而被放逐。神谕告诉Thyestes，他女儿Pelopia的儿子将会杀死Atreus。然而，Aegisthus（埃癸斯托斯）降生后他的母亲因为愧于乱伦将他遗弃。一位牧羊人发现了婴儿Aegisthus将他带给Atreus，Atreus认Aegisthus为儿子。当Aegisthus成年后，Thyestes告诉Aegisthus自己既是他的父亲也是他的祖父。Aegisthus杀掉Atreus，当时Atreus和Aerope已经有了两位儿子：Agamemnon和Menelaus还有一位女儿Anaxibia。</p>
                            <p>Agamemnon迎娶Clytemnestra，Menelaus迎娶Helen和她的妹妹（后世称她为特洛伊的海伦）。Troy王子Paris从Menelaus把Helen诱拐，Menelaus找到首领（也就是他的兄弟Agamemnon）希望他帮助夺回Helen。</p>
